package example.emre.com.emineproject;

public class Not {
    String tarih;
    String not;

    public Not(String tarih, String not) {
        this.tarih = tarih;
        this.not = not;
    }

    public String getTarih() {
        return tarih;
    }

    public void setTarih(String tarih) {
        this.tarih = tarih;
    }

    public String getNot() {
        return not;
    }

    public void setNot(String not) {
        this.not = not;
    }
}
